package DAY5;

public class LengthoftheGivenString {

	public static void main(String[] args) {

		String input = "ARUNRS";

		System.out.println(input.length());
		
		int count = 0 ;
		
		for (int i = 0; i < input.length(); i++) {
			count++;
			
		}
		System.out.println(count);


	}

}
